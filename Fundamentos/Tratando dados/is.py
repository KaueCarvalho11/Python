# Verifica se o valor informado é um número
a = input("Informe o valor a: ")
print(a.isnumeric())

# Verifica se o valor informado é composto apenas por letras
b =input("Informe o valor de b: ")
print(b.isalpha())

# Verifica se o valor informado é alfanumérico
c = input("Informe o valor de c: ")
print(c.isalnum())

# Verifica se o valor informado contém apenas letras maiúsculas
d = input("Informe o valor de d: ")
print(d.isupper)
